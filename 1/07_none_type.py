# Тема: Тип None
# -----------------
# None використовується для позначення відсутності значення ("нічого").
# Часто застосовується для ініціалізації змінної, коли реальне значення
# буде надано пізніше.

connect_to_database = None
print(connect_to_database)  # None

# Приклад типової перевірки на None
if connect_to_database is None:
    print("З'єднання з базою даних ще не встановлено")
