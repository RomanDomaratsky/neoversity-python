# Тема: Кортежі (Tuples)
# --------------------------
# Кортеж схожий на список, але є незмінним (immutable).

# Два способи створити порожній кортеж
my_tuple = tuple()
my_tuple = ()

# Створення непорожнього кортежу
my_tuple = (1, 2, 3)
print(my_tuple)  # (1, 2, 3)

# Кортеж з ОДНИМ елементом - обов'язково потрібна кома!
my_tuple = (1,)
print(my_tuple, type(my_tuple))  # (1,) <class 'tuple'>

# Без коми (1) - це просто число в дужках, а НЕ кортеж
not_a_tuple = (1)
print(not_a_tuple, type(not_a_tuple))  # 1 <class 'int'>

# Кортеж може містити різні типи даних
my_tuple = (1, "Hello", 3.14)
print(my_tuple)

# Створення кортежу без дужок (упакування кортежу)
my_tuple = 1, "Hello", 3.14
print(my_tuple)  # (1, 'Hello', 3.14)

# Доступ до елементів кортежу за допомогою індексації
first_item = my_tuple[0]  # 1
print(first_item)

# Кортежі незмінні - додавати/видаляти/змінювати елементи не можна
# my_tuple[0] = 100  # розкоментуй, щоб побачити TypeError

# Кортежі як ключі словника (наприклад, координати точок)
points = {
    (0, 0): "O",
    (1, 1): "A",
    (2, 2): "B",
}
print(points[(1, 1)])  # A
