# Тема: Порядок виконання операцій у виразі та коментарі
# -----------------------------------------------------------
# Пріоритет (від вищого до нижчого):
# 1. Дужки ()
# 2. Піднесення до степеня **
# 3. Унарні + та -
# 4. * / // %
# 5. Бінарні + та -
# 6. Оператори порівняння == != > < >= <=
# 7. Логічні оператори not, and, or

ratio = 10  # коефіцієнт корекції
result = 8 * (ratio + 5) - ratio ** 2
print(result)  # 20

# Приклад з пріоритетом заперечення та піднесення до степеня
print(-3 ** 2)    # -9  (спочатку 3**2, потім заперечення)
print((-3) ** 2)  # 9   (спочатку заперечення, потім піднесення до степеня)

# Приклад: відстань між двома точками (формула Евкліда)
x1 = 10
y1 = 10
x2 = 25
y2 = 25
d = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
print(d)  # 21.213203435596427
