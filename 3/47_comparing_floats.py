# Тема: Правильне порівняння дійсних чисел (math.isclose)
# ------------------------------------------------------------

import math

# Пряме порівняння дійсних чисел ненадійне через бінарне представлення
print(0.1 + 0.2 == 0.3)  # False
print(0.1 + 0.2)         # 0.30000000000000004

# math.isclose - порівняння з допустимою похибкою
r = math.isclose(0.1 + 0.2, 0.3)
print(r)  # True

# Порівняння з дуже малою похибкою
r = math.isclose(0.1, 0.10000000009)
print(r)  # True
