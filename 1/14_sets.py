# Тема: Множини (Sets)
# ------------------------
# Множина - неврегульований контейнер, який містить тільки унікальні елементи.
# У множину можна додавати тільки незмінні типи даних.

# Створення порожньої множини (тільки через set(), НЕ через {})
empty_set = set()

# Створення множини з ітерованого об'єкта (рядка)
a = set('hello')
print(a)  # {'h', 'e', 'l', 'o'} - порядок може відрізнятись

# Створення множини через фігурні дужки
b = {1, 2, 3, 4, 5}
print(b)  # {1, 2, 3, 4, 5}

# Унікальність: дублікати автоматично прибираються
numbers = {1, 2, 3, 1, 2, 3}
print(numbers)  # {1, 2, 3}

# Приклад: видалення дублікатів зі списку за допомогою множини
lst = [1, 2, 3, 1, 2, 2, 3, 4, 1]
d_lst = set(lst)
lst = list(d_lst)
print(lst)  # список без дублікатів (порядок може відрізнятись)

# add(elem) - додає елемент у множину
numbers = {1, 2, 3}
numbers.add(4)
print(numbers)  # {1, 2, 3, 4}

# remove(elem) - видаляє елемент, викидає помилку, якщо його немає
numbers = {1, 2, 3}
numbers.remove(3)
print(numbers)  # {1, 2}

# discard(elem) - видаляє елемент, НЕ викидає помилку, якщо його немає
numbers = {1, 2, 3}
numbers.discard(2)
print(numbers)  # {1, 3}


# --- Математичні операції над множинами ---
a = {1, 2, 3}
b = {3, 4, 5}

# Перетин: елементи, які є в обох множинах
print(a.intersection(b))  # {3}
print(a & b)               # {3}

# Різниця: елементи, які є в a, але немає в b
print(a.difference(b))     # {1, 2}
print(a - b)                # {1, 2}

# Симетрична різниця: елементи, які є тільки в одній із множин
print(a.symmetric_difference(b))  # {1, 2, 4, 5}
print(a ^ b)                       # {1, 2, 4, 5}

# Об'єднання: усі елементи з обох множин, без дублікатів
print(a.union(b))  # {1, 2, 3, 4, 5}
print(a | b)         # {1, 2, 3, 4, 5}
