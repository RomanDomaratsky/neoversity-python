# Тема: Вбудовані функції введення/виведення - print() та input()
# ---------------------------------------------------------------------

# print() - виведення одного значення
print("Hello world!")

# print() - декілька елементів через кому
x = 10
print("Значення x:", x)

# print() - краще використовувати f-рядок
print(f"Значення x: {x}")

# print() - параметр end (за замовчуванням переходить на новий рядок)
print("Привіт", end=" ")
print("Світ!")  # виведеться на тому самому рядку: "Привіт Світ!"

# input() - зчитування рядка з консолі, з підказкою
a = 21.1
print(f"Ви ввели: {a}")

# input() завжди повертає рядок (str), навіть якщо ввести число
print(type(a))  # <class 'str'>
