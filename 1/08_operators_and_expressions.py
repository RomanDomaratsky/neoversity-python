# Тема: Інструкції, вирази та арифметичні оператори
# -----------------------------------------------------

# Прості інструкції та вирази
x = 2
y = x + 10
print(y)  # 12

a = 1
b = 2
c = a + b + 10
print(c)  # 13

# Арифметичні оператори: + - * / % // **

# Приклад 1: теорема Піфагора (гіпотенуза прямокутного трикутника)
side_a = 10
side_b = 5
hypotenuse = (side_a ** 2 + side_b ** 2) ** 0.5
print(hypotenuse)  # 11.180339887498949

# Приклад 2: площа прямокутного трикутника
S = side_a * side_b / 2
print(S)  # 25.0 (тип float, навіть якщо операнди int)

# Приклад 3: переведення секунд у години, хвилини, секунди
n = 5000
hours = n // (60 * 60)
minutes = (n - hours * 60 * 60) // 60
seconds = n - hours * 60 * 60 - minutes * 60
print(f"{hours} год {minutes} хв {seconds} с")  # 1 год 23 хв 20 с

# Приклад 4: залишок від ділення (оператор %)
print(10 % 3)  # 1
print(15 % 4)  # 3
print(7 % 5)   # 2

# Перевірка числа на парність/непарність за допомогою %
x = 7
if x % 2 == 0:
    print(f"{x} - парне число")
else:
    print(f"{x} - непарне число")
