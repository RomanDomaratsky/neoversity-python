# Тема: Цикли та словники (ітерування за словником)
# --------------------------------------------------------

numbers = {
    1: "one",
    2: "two",
    3: "three"
}

# Ітерація за словником - перебирає КЛЮЧІ
for key in numbers:
    print(key)
# 1
# 2
# 3

# Те саме явно через метод keys()
for key in numbers.keys():
    print(key)
# 1
# 2
# 3

# Перебір ЗНАЧЕНЬ словника через values()
for val in numbers.values():
    print(val)
# one
# two
# three

# Перебір пар (ключ, значення) через items()
for key, value in numbers.items():
    print(key, value)
# 1 one
# 2 two
# 3 three

# ВАЖЛИВО: під час ітерації за словником не можна додавати чи
# видаляти елементи. Можна тільки перезаписувати значення за ключем.
