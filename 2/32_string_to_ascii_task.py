# Тема: Задача на функцію - конвертація символів рядка в коди ASCII
# ------------------------------------------------------------------------
# Використовуємо: словник, функцію ord(), цикл for, оформлення у функцію.


def string_to_codes(string: str) -> dict:
    # Ініціалізація словника для зберігання кодів
    codes = {}
    # Перебір кожного символу в рядку
    for ch in string:
        # Перевірка, чи символ уже є в словнику
        if ch not in codes:
            # Додавання пари символ-код у словник
            codes[ch] = ord(ch)
    return codes


result = string_to_codes("Hello world!")
print(result)
# {'H': 72, 'e': 101, 'l': 108, 'o': 111, ' ': 32, 'w': 119,
#  'r': 114, 'd': 100, '!': 33}
